  function readLang() {
    try { var l = localStorage.getItem('staffup-lang'); if (l === 'fr' || l === 'en') return l; } catch (_) {}
    return 'fr';
  }

  function t(key) {
    var lang = window.STAFFUP_LANG || 'fr';
    return (TRANS[lang] && TRANS[lang][key]) || (TRANS['fr'] && TRANS['fr'][key]) || key;
  }

  function applyAll() {
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var v = t(el.getAttribute('data-i18n'));
      if (v) el.textContent = v;
    });
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var v = t(el.getAttribute('data-i18n-html'));
      if (v) el.innerHTML = v;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var v = t(el.getAttribute('data-i18n-placeholder'));
      if (v) el.placeholder = v;
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
      var v = t(el.getAttribute('data-i18n-title'));
      if (v) el.title = v;
    });
    document.documentElement.lang = window.STAFFUP_LANG === 'en' ? 'en' : 'fr';
    document.querySelectorAll('.lang-toggle').forEach(function (btn) {
      btn.textContent = t('lang.toggle');
      btn.title = t('lang.title');
    });
    // Re-render hooks for JS-generated tables that can't use data-i18n
    if (window.staffupRerenderPool)      window.staffupRerenderPool();
    if (window.staffupRerenderClients)   window.staffupRerenderClients();
    if (window.staffupRerenderAgents)    window.staffupRerenderAgents();
    if (window.staffupRerenderJournal)   window.staffupRerenderJournal();
    if (window.staffupRerenderFacture)   window.staffupRerenderFacture();
    if (window.staffupRerenderDemandes)  window.staffupRerenderDemandes();
    if (window.staffupRerenderDashboard) window.staffupRerenderDashboard();
    if (window.staffupRerenderGallery)   window.staffupRerenderGallery();
  }

  window.STAFFUP_LANG = readLang();
  window.STAFFUP_T = t;
  window.STAFFUP_APPLY_LANG = applyAll;

  window.STAFFUP_TOGGLE_LANG = function () {
    var next = window.STAFFUP_LANG === 'fr' ? 'en' : 'fr';
    window.STAFFUP_LANG = next;
    try { localStorage.setItem('staffup-lang', next); } catch (_) {}
    applyAll();
    if (window.STAFFUP_TOAST) {
      window.STAFFUP_TOAST(t('toast.lang'));
    }
  };
